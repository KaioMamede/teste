# Faça uma calculado em python que solicite ao usuário dois valores e, em seguida imprima as operações matematicas de soma, subtração,

a = float(input("Digite a 1° valor: "))
b = float(input("Digite a 2° valor: "))
print("A soma é ", (a+b))
print("A subtração é ", (a-b))
print("A multiplicão é ", (a*b))
print("A divisão é: ", (a/b))
print("O resto da divisão é: ", (a%b))